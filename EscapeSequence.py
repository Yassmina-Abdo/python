# Escape Sequences char
# \b => Back space  (Removes one char)
# \  => Escape New line
# \n => New line Feed
# \t => Horizontal Tab
# \r => Carriage return


# Back Space
print("Hello\b\b World")

print("""
Hello 
from 
Python
""")

# Escape New Line
print("""\
Hello \
from \
Python \
""")

# Escape " "
print("My Name is \"Abdelrahman\"")
print('My Name is "Abdelrahman"')

# Escape ' '
print('My Name is \'Abdelrahman\'')
print("My Name is 'Abdelrahman'")


# Escape Back Slash
print("My Name is \\'Abdelrahman'")

# New line Feed
print("My Name is \n'Abdelrahman'")

# Horizontal Tab
print("My Name is \t'Abdelrahman'")

# Carriage return
print("123456\rABCDE")
