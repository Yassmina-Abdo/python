print(type(10))   # int ->Integer
print(type(100))  # int ->Integer
print(type(-50))  # int ->Integer

print(type(100.5))       # float-> Floating point
print(type(1.32847475))  # float-> Floating point
print(type(-7.5))        # float-> Floating point

print(type("Hello World"))  # Str -> String

print(type([1, 2, 3, 4, 5]))   # list -> List

print(type((1, 2, 3, 4, 5)))   # tuple -> Tuple

print(type({"one": 1, "Two": 2}))   # dict -> Dictionary

print(type(True))   # bool -> Boolean
